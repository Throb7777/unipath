def normalize_lang(value: str | None) -> str:
    if not value:
        return DEFAULT_LANG
    raw = value.strip().lower()
    if raw.startswith("zh"):
        return "zh-CN"
    if raw.startswith("en"):
        return "en"
    return DEFAULT_LANG


def resolve_lang(request) -> str:
    query_lang = request.query_params.get("lang")
    if query_lang:
        return normalize_lang(query_lang)
    cookie_lang = request.cookies.get("relay_lang")
    if cookie_lang:
        return normalize_lang(cookie_lang)
    accept_language = request.headers.get("accept-language", "")
    if accept_language:
        return normalize_lang(accept_language.split(",")[0])
    return DEFAULT_LANG


def make_translator(lang: str) -> Callable[..., str]:
    normalized = normalize_lang(lang)
    catalog = MESSAGES.get(normalized, MESSAGES[DEFAULT_LANG])
    fallback = MESSAGES[DEFAULT_LANG]

    def t(key: str, **kwargs) -> str:
        value = catalog.get(key, fallback.get(key, key))
        if kwargs:
            return value.format(**kwargs)
        return value

    return t


def page_url(path: str, lang: str, **params: str) -> str:
    query: dict[str, str] = {"lang": normalize_lang(lang)}
    for key, value in params.items():
        if value:
            query[key] = value
    return f"{path}?{urlencode(query)}"


def switch_lang_url(request, target_lang: str) -> str:
    query = dict(request.query_params)
    query["lang"] = normalize_lang(target_lang)
    return f"{request.url.path}?{urlencode(query)}"
